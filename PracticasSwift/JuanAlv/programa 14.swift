//Description: Determinar cual cantidad es mayor: 3^75 o 2^100

//Input: -

//Output: mayor

if pow(3,75)==pow(2,100){
	print("3^75 es igual que 2^100")
	}
else{
	if pow(3,75)>pow(2,100){
		print("3^75 es mayor que 2^100")
	}
	else{
		print("2^100 es mayor que 3^75")
	}
}
